/**
 * Writes frontend/public/env.js from process.env.API_URL (Vercel/CI).
 * Falls back to the known Railway production URL when unset.
 *
 * Usage: node scripts/write-frontend-env.mjs
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const outPath = path.join(__dirname, '..', 'frontend', 'public', 'env.js');

const apiUrl =
  (process.env.API_URL || '').trim() ||
  'https://digital-paladin-gamification-system-production.up.railway.app';

if (!/^https?:\/\//i.test(apiUrl)) {
  console.error(`[write-frontend-env] Invalid API_URL: ${apiUrl}`);
  process.exit(1);
}

const contents = `/**
 * Generated by scripts/write-frontend-env.mjs — do not edit by hand in CI.
 * window.__ENV__.API_URL is read by src/environments/environment.prod.ts.
 */
window.__ENV__ = window.__ENV__ || {};
window.__ENV__.API_URL = ${JSON.stringify(apiUrl)};
`;

fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, contents, 'utf8');
console.log(`[write-frontend-env] Wrote ${outPath}`);
console.log(`[write-frontend-env] API_URL=${apiUrl}`);
